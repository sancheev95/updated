int	ft_is_prime(int nb)
{
	int i;
	int	check;

	check = 0;
	i = 2;
	while (i <= nb / 2)
	{
		if (nb % i == 0)
		{
			check = 1;
			break ;
		}
		i++;
	}
	if (nb == 1)
	{
		return (0);
	}
	else
	{
		if (check == 0)
			return (1);
		else
			return (0);
	}
}
